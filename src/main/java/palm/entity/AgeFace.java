package palm.entity;

public class AgeFace {

    private int age;
    private int start;

    public AgeFace(int age, int start) {
        this.age = age;
        this.start = start;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getStart() {
        return start;
    }

    public void setStart(int start) {
        this.start = start;
    }
}
